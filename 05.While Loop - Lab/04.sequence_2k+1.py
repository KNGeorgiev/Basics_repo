# Напишете програма, която чете число n, въведено от потребителя,
# и отпечатва всички числа  ≤  n от редицата: 1, 3, 7, 15, 31, ….
# Всяко следващо число се изчислява като умножим предишното с 2 и добавим 1.

counter = int(input())
num = 1

while num <= counter:
    print(num)
    num = 2 * num + 1